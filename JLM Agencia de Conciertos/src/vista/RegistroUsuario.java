    private void jTextField_Registro_NIFKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_Registro_NIFKeyReleased

        String nif = this.jTextField_Registro_NIF.getText();
        jLabel_Registro_tick_verde3.setVisible(false);
        jLabel_Registro_cruz_roja3.setVisible(false);
        if (nif.isEmpty()) {
            this.jLabel_Registro_NifCorrecto.setText("");
        } else if (nif.length() != 9) {
            this.jLabel_Registro_NifCorrecto.setText("Debe contener 9 caracteres exactos");
            jLabel_Registro_cruz_roja3.setVisible(true);
        } else {
            String numeros = "", letra = "";
            for (int i = 0; i < nif.length() - 1; i++) {
                numeros += nif.charAt(i);
            }
            letra += nif.charAt(8);
            this.jLabel_Registro_NifCorrecto.setText("");
            if (!numeros.matches("[0-9]*")) {
                this.jLabel_Registro_NifCorrecto.setText("Los primeros 8 caracteres deben de ser numeros");
                jLabel_Registro_cruz_roja3.setVisible(true);
            } else if (!letra.matches(".*[A-Z].*")) {
                this.jLabel_Registro_NifCorrecto.setText("El ultimo caracter debe ser una letra mayuscula");
                jLabel_Registro_cruz_roja3.setVisible(true);
            } else {
                this.jLabel_Registro_NifCorrecto.setText("Correcto");
                jLabel_Registro_cruz_roja3.setVisible(false);
                jLabel_Registro_tick_verde3.setVisible(true);
            }
        }
    }//GEN-LAST:event_jTextField_Registro_NIFKeyReleased


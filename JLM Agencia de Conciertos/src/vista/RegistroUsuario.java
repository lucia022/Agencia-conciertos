    private void jTextField_Registro_NombreKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_Registro_NombreKeyReleased
        String nombre = this.jTextField_Registro_Nombre.getText();
        jLabel_tick_verde.setVisible(false);
        jLabel_Registro_cruz_roja.setVisible(false);
        if (nombre.isEmpty()) {
            this.jLabel_Registro_NombreCorrecto.setText("");
        } else if ((nombre.length() < 3)) {
            jLabel_Registro_cruz_roja.setVisible(true);
            this.jLabel_Registro_NombreCorrecto.setText("Debe de contener al menos 3 caracteres");
        } else {
            this.jLabel_Registro_NombreCorrecto.setText("");
            if ((nombre.length() > 13)) {
                this.jLabel_Registro_NombreCorrecto.setText("No puede contener mas de 13 caracteres");
                jLabel_Registro_cruz_roja.setVisible(true);
            } else {
                this.jLabel_Registro_NombreCorrecto.setText("");
                if (nombre.matches(".*[^a-z-A-Z].*")) {
                    this.jLabel_Registro_NombreCorrecto.setText("No puede contener caracteres especiales");
                    jLabel_Registro_cruz_roja.setVisible(true);
                } else {
                    this.jLabel_Registro_NombreCorrecto.setText("Correcto");
                    jLabel_Registro_cruz_roja.setVisible(false);
                    jLabel_tick_verde.setVisible(true);
                }
            }

        }
    }//GEN-LAST:event_jTextField_Registro_NombreKeyReleased

